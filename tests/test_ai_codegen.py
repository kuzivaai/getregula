class TestGovernance:
    def test_empty_project_score_zero(self):
        with tempfile.TemporaryDirectory() as td:
            gov = _assess_governance(td)
            assert gov["score"] == 0
            assert gov["has_ai_policy"] is False
            assert gov["has_disclosure"] is False
            assert gov["has_model_card"] is False
            assert gov["has_tool_inventory"] is False

    def test_policy_only_score_40(self):
        with tempfile.TemporaryDirectory() as td:
            Path(td, "ai-policy.md").write_text("# AI Policy")
            gov = _assess_governance(td)
            assert gov["score"] == 40
            assert gov["has_ai_policy"] is True

    def test_full_governance_score_100(self):
        with tempfile.TemporaryDirectory() as td:
            Path(td, "ai-policy.md").write_text("# AI Policy")
            Path(td, "AI_DISCLOSURE.md").write_text("# Disclosure")
            Path(td, "MODEL_CARD.md").write_text("# Model Card")
            Path(td, "AI_TOOLS.md").write_text("# Tools")
            gov = _assess_governance(td)
            assert gov["score"] == 100

    def test_docs_subdir_policy(self):
        with tempfile.TemporaryDirectory() as td:
            docs = Path(td, "docs")
            docs.mkdir()
            (docs / "AI_GOVERNANCE.md").write_text("# Gov")
            gov = _assess_governance(td)
            assert gov["has_ai_policy"] is True


