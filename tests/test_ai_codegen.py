class TestFullScan:
    def test_empty_project(self):
        with tempfile.TemporaryDirectory() as td:
            result = scan_ai_generated_code(td, include_git=False)
            assert result["markers_found"] == []
            assert result["tool_configs"] == []
            assert result["git_signals"] == []
            assert result["summary"]["generators_detected"] == []
            assert result["summary"]["files_with_markers"] == 0
            assert result["summary"]["transparency_compliant"] is False
            assert "limitations" in result
            assert "explicit markers only" in result["limitations"]

    def test_project_with_markers(self):
        with tempfile.TemporaryDirectory() as td:
            Path(td, "app.py").write_text("# Generated by GitHub Copilot\nx = 1\n")
            Path(td, "clean.py").write_text("def f(): pass\n")
            result = scan_ai_generated_code(td, include_git=False)
            assert result["summary"]["files_with_markers"] == 1
            assert result["summary"]["total_files_scanned"] == 2
            assert "GitHub Copilot" in result["summary"]["generators_detected"]

    def test_project_with_config_and_markers(self):
        with tempfile.TemporaryDirectory() as td:
            Path(td, ".cursorrules").write_text("rules")
            Path(td, "main.py").write_text("# Generated with Claude\npass\n")
            result = scan_ai_generated_code(td, include_git=False)
            gens = result["summary"]["generators_detected"]
            assert "Claude" in gens
            assert "Cursor" in gens

    def test_fully_governed_project(self):
        with tempfile.TemporaryDirectory() as td:
            Path(td, "ai-policy.md").write_text("# Policy")
            Path(td, "AI_DISCLOSURE.md").write_text("# Disclosure")
            Path(td, "MODEL_CARD.md").write_text("# Card")
            Path(td, "AI_TOOLS.md").write_text("# Tools")
            result = scan_ai_generated_code(td, include_git=False)
            assert result["summary"]["governance_score"] == 100
            assert result["summary"]["transparency_compliant"] is True

    def test_limitations_always_present(self):
        with tempfile.TemporaryDirectory() as td:
            result = scan_ai_generated_code(td, include_git=False)
            assert "limitations" in result
            assert len(result["limitations"]) > 0


