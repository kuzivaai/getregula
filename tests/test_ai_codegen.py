        findings = _scan_file_for_markers(content, "app.py")
        assert len(findings) == 1, f"expected exactly 1 finding, got {len(findings)}"
        assert findings[0]["generator"] == "GitHub Copilot"
        assert findings[0]["provider"] == "Microsoft/GitHub"
