def format_ai_codegen_text(result: dict) -> str:
    """Format scan results as human-readable text."""
    lines = []
    summary = result["summary"]
    gov = result["governance_status"]

    # Header
    lines.append("=" * 60)
    lines.append("AI-Generated Code Governance Report")
    lines.append("=" * 60)
    lines.append("")

    # Generators detected
    if summary["generators_detected"]:
        gen_list = ", ".join(summary["generators_detected"])
        lines.append(f"AI Code Generators Detected: {gen_list}")
        lines.append(f"  Files with explicit markers: {summary['files_with_markers']}")
        lines.append(f"  Total files scanned: {summary['total_files_scanned']}")
    else:
        lines.append("No AI code generators detected (explicit markers only).")
        lines.append(f"  Total files scanned: {summary['total_files_scanned']}")
    lines.append("")

    # Markers
    if result["markers_found"]:
        lines.append("--- Explicit Markers Found ---")
        for m in result["markers_found"]:
            lines.append(f"  {m['file']}:{m['line']} [{m['generator']}] {m['marker_text']}")
        lines.append("")

    # Tool configs
    if result["tool_configs"]:
        lines.append("--- AI Tool Configurations ---")
        for t in result["tool_configs"]:
            lines.append(f"  {t['config_path']} ({t['tool']} — {t['provider']})")
        lines.append("")

    # Git signals
    if result["git_signals"]:
        lines.append("--- Git Commit Signals ---")
        for g in result["git_signals"]:
            lines.append(f"  {g['commit']}: [{g['generator']}] {g['message_excerpt']}")
        lines.append("")

    # Governance score
    score = gov["score"]
    if score >= 70:
        score_label = "GOOD"
    elif score >= 40:
        score_label = "PARTIAL"
    else:
        score_label = "LOW"
    lines.append(f"Governance Score: {score}/100 ({score_label})")
    lines.append(f"  AI usage policy:    {'Yes' if gov['has_ai_policy'] else 'No'}")
    lines.append(f"  AI code disclosure: {'Yes' if gov['has_disclosure'] else 'No'}")
    lines.append(f"  Model card:         {'Yes' if gov['has_model_card'] else 'No'}")
    lines.append(f"  Tool inventory:     {'Yes' if gov['has_tool_inventory'] else 'No'}")
    lines.append("")

    # Transparency compliance
    if summary["transparency_compliant"]:
        lines.append("EU AI Act Transparency (Art 50/52): COMPLIANT")
        lines.append("  AI usage policy and disclosure documentation found.")
    else:
        lines.append("EU AI Act Transparency (Art 50/52): GAPS FOUND")
        if not gov["has_ai_policy"]:
            lines.append("  Missing: AI usage policy — create docs/AI_USAGE_POLICY.md documenting")
            lines.append("    which AI tools are used, for what purposes, and review procedures.")
        if not gov["has_disclosure"]:
            lines.append("  Missing: AI code disclosure — create docs/AI_DISCLOSURE.md listing")
            lines.append("    AI-generated or AI-assisted code with provenance information.")
    lines.append("")

    # Recommendations
    recommendations = []
    if not gov["has_ai_policy"]:
        recommendations.append("Create docs/AI_USAGE_POLICY.md with: approved tools, usage guidelines, review requirements.")
    if not gov["has_disclosure"]:
        recommendations.append("Create docs/AI_DISCLOSURE.md with: list of AI-generated files, generator used, date, reviewer.")
    if not gov["has_model_card"]:
        recommendations.append("Create docs/MODEL_CARD.md if deploying ML models (dataset, performance, limitations).")
    if not gov["has_tool_inventory"]:
        recommendations.append("Create docs/AI_TOOLS.md with: inventory of AI tools, versions, configurations.")
    if summary["generators_detected"] and not gov["has_ai_policy"]:
        recommendations.append("AI tools are in use but no governance policy exists. This is a transparency gap under Art 50.")

    if recommendations:
        lines.append("--- Recommendations ---")
        for i, rec in enumerate(recommendations, 1):
            lines.append(f"  {i}. {rec}")
        lines.append("")

    # Limitations
    lines.append("--- Limitations ---")
    lines.append(f"  {result['limitations']}")
    lines.append("")

    return "\n".join(lines)
