def _read_file(filepath: str) -> Optional[str]:
    """Read file content, returning None on failure."""
    try:
        return Path(filepath).read_text(encoding="utf-8", errors="ignore")
    except (PermissionError, OSError):
        return None


def _walk_code_files(project_path: str):
    """Yield (relative_path, absolute_path) for code files."""
    project = Path(project_path).resolve()
    for root, dirs, files in os.walk(project):
        dirs[:] = [d for d in dirs if d not in SKIP_DIRS]
        for filename in files:
            filepath = Path(root) / filename
            if filepath.suffix.lower() in CODE_EXTENSIONS:
                try:
                    rel = str(filepath.relative_to(project))
                except ValueError:
                    rel = str(filepath)
                yield rel, str(filepath)


def _scan_file_for_markers(content: str, rel_path: str):
    """Scan file content for explicit AI generator markers."""
    findings = []
    lines = content.splitlines()
    for marker_id, marker_info in EXPLICIT_MARKERS.items():
        for pattern in marker_info["patterns"]:
            for line_num, line in enumerate(lines, 1):
                if re.search(pattern, line, re.IGNORECASE):
                    findings.append({
                        "file": rel_path,
                        "line": line_num,
                        "generator": marker_info["generator"],
                        "provider": marker_info["provider"],
                        "confidence": "high",
                        "marker_text": line.strip(),
                    })
    return findings


def _detect_tool_configs(project_path: str):
    """Check for AI tool configuration files/directories."""
    project = Path(project_path).resolve()
    found = []
    for cfg in TOOL_CONFIGS:
        target = project / cfg["path"]
        if target.exists():
            found.append({
                "tool": cfg["tool"],
                "config_path": cfg["path"],
                "provider": cfg["provider"],
            })
    return found


def _scan_git_log(project_path: str, max_commits: int = 200):
    """Scan recent git commits for AI-generated code signals."""
    signals = []
    try:
        result = subprocess.run(
            ["git", "log", f"--max-count={max_commits}", "--format=%H %s%n%b"],
            capture_output=True, text=True, timeout=10,
            cwd=project_path,
        )
        if result.returncode != 0:
            return signals
    except (FileNotFoundError, subprocess.TimeoutExpired, OSError):
        return signals

    return _parse_git_output(result.stdout)


def _parse_git_output(git_output: str):
    """Parse git log output for AI signals. Separated for testability."""
    signals = []
    current_hash = None
    for line in git_output.splitlines():
        parts = line.split(" ", 1)
        if len(parts) == 2 and len(parts[0]) == 40 and all(c in "0123456789abcdef" for c in parts[0]):
            current_hash = parts[0][:8]
        for gp in GIT_COMMIT_PATTERNS:
            if re.search(gp["pattern"], line, re.IGNORECASE):
                signals.append({
                    "commit": current_hash or "unknown",
                    "generator": gp["generator"],
                    "message_excerpt": line.strip()[:120],
                })
    return signals


def _assess_governance(project_path: str):
    """Assess governance documentation quality."""
    project = Path(project_path).resolve()

    def _any_exists(file_list):
        return any((project / f).exists() for f in file_list)

    has_policy = _any_exists(POLICY_FILES)
    has_disclosure = _any_exists(DISCLOSURE_FILES)
    has_model_card = _any_exists(MODEL_CARD_FILES)
    has_inventory = _any_exists(TOOL_INVENTORY_FILES)

    score = 0
    if has_policy:
        score += 40
    if has_disclosure:
        score += 30
    if has_model_card:
        score += 15
    if has_inventory:
        score += 15

    return {
        "has_ai_policy": has_policy,
        "has_disclosure": has_disclosure,
        "has_model_card": has_model_card,
        "has_tool_inventory": has_inventory,
        "score": score,
    }


