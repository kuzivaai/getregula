def scan_ai_generated_code(project_path: str, include_git: bool = True) -> dict:
    """Scan project for AI-generated code and assess governance.

    IMPORTANT: This scanner detects EXPLICIT markers only.
    It does not use stylistic heuristics, which have high
    false-positive rates. Absence of markers does not mean
    code is human-written.

    Returns:
        Dict with markers_found, tool_configs, git_signals,
        governance_status, summary, and limitations.
    """
    markers_found = []
    total_files = 0

    for rel_path, abs_path in _walk_code_files(project_path):
        total_files += 1
        content = _read_file(abs_path)
        if content is None:
            continue
        markers_found.extend(_scan_file_for_markers(content, rel_path))

    tool_configs = _detect_tool_configs(project_path)

    git_signals = []
    if include_git:
        git_signals = _scan_git_log(project_path)

    governance = _assess_governance(project_path)

    generators = sorted(set(
        [m["generator"] for m in markers_found]
        + [t["tool"] for t in tool_configs]
        + [g["generator"] for g in git_signals]
    ))

    files_with_markers = len(set(m["file"] for m in markers_found))

    return {
        "markers_found": markers_found,
        "tool_configs": tool_configs,
        "git_signals": git_signals,
        "governance_status": governance,
        "summary": {
            "generators_detected": generators,
            "files_with_markers": files_with_markers,
            "total_files_scanned": total_files,
            "governance_score": governance["score"],
            "transparency_compliant": governance["has_ai_policy"] and governance["has_disclosure"],
        },
        "limitations": (
            "This scanner detects explicit markers only. "
            "AI-generated code without markers will not be detected. "
            "For comprehensive tracking, implement a code provenance policy."
        ),
    }


